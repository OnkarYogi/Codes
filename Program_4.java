public class Program_4 
{
    public static void main(String[] args)
    {
        boolean bool_var = true; // can contain 'true' or 'false' value
        byte byte_var = 10;
        short short_var = 1000;
        int int_var = 987987;
        long long_var = 664363467437l; // using 'l' since it's a long variable
        char char_var = 'A'; // character should be in single inverted commas
        float float_var = 536453.6363f; // using 'f' since it's a float variable
        double double_var = 63623.76236236; // default double variable (optional: can use 'D' or 'd')
        String string_var = "Onkar";
        
        System.out.println(bool_var);
        System.out.println(byte_var);
        System.out.println(short_var);
        System.out.println(int_var);
        System.out.println(long_var);
        System.out.println(char_var);
        System.out.println(float_var);
        System.out.println(double_var);
        System.out.println(string_var);

    }
}
